<?php
namespace App\Models;

class UserRole extends Model {
    
    
    protected $primaryKey = 'id';
    
    public $table = 'user_role';
    public $timestamps = false;
    
    /**
     * Variaveis seguras para uso e guardar dados 
     * @var array 
     */
    public $fillable = [
        'id',
        'user_id',
        'role_id',
    ];
    
    /**
     * Tipos nativos dos atributos
     * @var array
     */
    protected $casts = [
        'id' => 'integer',
        'user_id' => 'integer',
        'role_id' => 'integer',
    ];    
    
	
    public function Role() {
		
        return $this->hasOne('App\Models\Role', 'id', 'role_id')->withDefault();
    }

    /**
     * Busca o modelo de acls     
	 * 
	 * @return acls     
	 */
    public function Roles() {
        return $this->hasMany('App\Models\Role', 'id', 'role_id');
    }
    /**
     * Busca o modelo de users     
	 * 
	 * @return users     
	 */
    public function Users() {
        return $this->belongsTo('App\Models\Users', 'id', 'user_id');
    }

    /**
     * Verifica se o usuÃ¡rio tem permissÃ£o pra acessar o registro
     *
     * @return null
     */
    public function authorize(){
    
        if(!empty($this->id)) {

            $builder = self::select(); 
            $builder->where('id', $this->id);
            //$builder->where('user_id', \Auth::user()->id);

            // Grava em laravel.log
            //
            //\Log::info($builder->getBindings());
            //\Log::info($builder->toSql());

            if($builder->count() <= 0){
                die(view('default/403')->render());
            }
        } 
    }
    
    
    /**
     * Realiza a consulta da tabela
     *
     * @param array $filter
     * @return \Illuminate\Support\Collection
     */
    public function fetch(array $filter = [], $expression = '*') {
        
        if(empty($filter)) $filter = $this->toArray();
    
        $builder = self::selectRaw($expression);

           
        if(array_key_exists('id', $filter) && !empty($filter['id'])) {
            $builder->where('id', $filter['id']);
        }
           
        if(array_key_exists('user_id', $filter) && !empty($filter['user_id'])) {
            $builder->where('user_id', $filter['user_id']);
        }
           
        if(array_key_exists('role_id', $filter) && !empty($filter['role_id'])) {
            $builder->where('role_id', $filter['role_id']);
        }
        
        
        if(array_key_exists('orderBy', $filter) && !empty($filter['orderBy'])) {
            $builder->orderBy($filter['orderBy'], 'ASC');
        }
        
        // Grava em laravel.log
        //
        //\Log::info($builder->getBindings());
        //\Log::info($builder->toSql());

        return $builder;
    }
}